package com.cg.capbook.exceptions;

public class IncorrectSecurityQuetsionException extends Exception {

	public IncorrectSecurityQuetsionException() {
		super();
	}

	public IncorrectSecurityQuetsionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public IncorrectSecurityQuetsionException(String message, Throwable cause) {
		super(message, cause);
	}

	public IncorrectSecurityQuetsionException(String message) {
		super(message);
	}

	public IncorrectSecurityQuetsionException(Throwable cause) {
		super(cause);
	}

}
